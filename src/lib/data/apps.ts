export const APPS_BY_CATEGORY: Record<AppCategory, App[]> = {
	browsers: BROWSER_APPS,
	development: DEVELOPMENT_APPS,
	productivity: PRODUCTIVITY_APPS,
	communication: COMMUNICATION_APPS,
	media: MEDIA_APPS,
	utilities: UTILITY_APPS,
	security: SECURITY_APPS,
	design: DESIGN_APPS,
};

export function getAppsByCategory(category: AppCategory): App[] {
	const apps = APPS_BY_CATEGORY[category];
	return apps ? [...apps] : [];
}

export function generateBrewCommand(appIds: string[]): string {
	if (appIds.length === 0) return "";

	const apps = appIds
		.map((id) => APPS.find((app) => app.id === id))
		.filter((app): app is App => app !== undefined);

	const brewNames = apps.map((app) => app.brewName).join(" ");
	return `brew install --cask ${brewNames}`;
}
